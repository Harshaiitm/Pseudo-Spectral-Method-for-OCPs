function [lb, ub] = Three_dimensional_lower_upper_bounds(M,problem)

m0 = problem.m0;
Thrust_max = problem.Thrust_max;
Thrust_max_2 = problem.Thrust_max_2;
m0_2 = problem.m0_2;


lb(1:M) = -inf;
lb(M+1:2*M) = -inf;
lb(2*M+1:3*M) = -inf;
lb(3*M+1:4*M) = -inf;
lb(4*M+1:5*M) = -inf;
lb(5*M+1:6*M) = -inf;
lb(6*M+1:7*M) = 100;
lb(7*M+1:8*M) = 0;
lb(8*M+1:9*M) = 0;
lb(9*M+1:10*M) = 0;
lb(10*M+1:11*M) = -inf;
lb(11*M+1:12*M) = -inf;
lb(12*M+1:13*M) = -inf;
lb(13*M+1:14*M) = -inf;
lb(14*M+1:15*M) = -inf;
lb(15*M+1:16*M) = -inf;
lb(16*M+1:17*M) = 100;
lb(17*M+1:18*M) = 0;
lb(18*M+1:19*M) = 0;
lb(19*M+1:20*M) = 0;
lb(20*M+1:21*M) = -1;
lb(21*M+1:22*M) = -1;
lb(22*M+1:23*M) = -1;
lb(23*M+1:24*M) = -1;
lb(24*M+1) = 0;
lb(24*M+2) = 0;

ub(1:M) = inf;
ub(M+1:2*M) = inf;
ub(2*M+1:3*M) = inf;
ub(3*M+1:4*M) = inf;
ub(4*M+1:5*M) = inf;
ub(5*M+1:6*M) = inf;
ub(6*M+1:7*M) = m0;
ub(7*M+1:8*M) = Thrust_max;
ub(8*M+1:9*M) = Thrust_max;
ub(9*M+1:10*M) = Thrust_max;
ub(10*M+1:11*M) = inf;
ub(11*M+1:12*M) = inf;
ub(12*M+1:13*M) = inf;
ub(13*M+1:14*M) = inf;
ub(14*M+1:15*M) = inf;
ub(15*M+1:16*M) = inf;
ub(16*M+1:17*M) = m0_2;
ub(17*M+1:18*M) = Thrust_max_2;
ub(18*M+1:19*M) = Thrust_max_2;
ub(19*M+1:20*M) = Thrust_max_2;
ub(20*M+1:21*M) = 1;
ub(21*M+1:22*M) = 1;
ub(22*M+1:23*M) = 1;
ub(23*M+1:24*M) = 1;
ub(24*M+1) = inf;
ub(24*M+2) = inf;
end